   private void HienThi_DSHH() {
        ArrayList<HangHoa> dsNCC = HangHoaDAO.layDSHangHoa();
        DefaultTableModel dtb = (DefaultTableModel)tbl_danhsachHanghoa.getModel();
        dtb.setRowCount(0);
        for(HangHoa ncc : dsNCC)
        {
            Vector<Object> vec = new Vector<Object>();
            vec.add(ncc.getMaHH());
            vec.add(ncc.getTenHH());
            vec.add(ncc.getDVT());
            vec.add(ncc.getGiaban());
            vec.add(ncc.getSlton());            
            vec.add(ncc.getTENNH());                       
            dtb.addRow(vec);
        }
        tbl_danhsachHanghoa.setModel(dtb);
        
        
    }

    
