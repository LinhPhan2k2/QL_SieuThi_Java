    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
    if(txt_giaban.getText().isEmpty() || txt_soluong.getText().isEmpty()||txt_tenHH.getText().isEmpty())
    {
      JOptionPane.showMessageDialog(jPanel4, "Vui lòng nhập đầy đủ thông tin");
    }  
    else
    {              
        try {
                 double giaBan= Double.parseDouble(txt_giaban.getText());
                 int sl= Integer.parseInt(txt_soluong.getText());                   
                 if(giaBan<0||sl<0)
                 {
                        JOptionPane.showMessageDialog(jPanel4, "Không được nhập số âm");
                 }
                 else{
                    HangHoa hh = new HangHoa();     
                    ArrayList<String> tenNH= new ArrayList<String>();
                    tenNH=HangHoaDAO.layDSTenNH();
                    int pos=0;
                    for(int i=0; i<tenNH.size();i++)
                    {                     
                        if(cbo_tenLoaiHH.getSelectedItem().toString().trim().equals(tenNH.get(i).trim())==true)
                        {
                           pos=i+1;                            
                           break;
                        }
                    }                                                                                             
                    try {                           
                        int temp= Integer.parseInt(txt_soluong.getText());
                        double giaban=Double.parseDouble(txt_giaban.getText());
                        
                        hh.setSlton(temp);
                        hh.setGiaban(giaban);
                        hh.setDVT(cbo_dvt.getSelectedItem().toString()); 
                        hh.setMaNH(pos);
                        hh.setTenHH(txt_tenHH.getText());
                        boolean kq= HangHoaDAO.themHH(hh);                    
                        if(kq)
                        {
                          JOptionPane.showMessageDialog(jPanel4, "Thêm thành công");
                        }
                        else
                        {
                          JOptionPane.showMessageDialog(jPanel4, "Thêm thất bại");
                        }
                    
                    }
                    catch (Exception e) {
                        JOptionPane.showMessageDialog(jPanel4, "Số lượng và đơn giá phải là số");
                 }
                 }
       
                                  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jPanel4, "Số lượng và đơn giá phải là số");
        }
        HienThi_DSHH();
         updateRowHeights();
                                                                                                                                    
    }                            
    }//GEN-LAST:event_btn_luuActionPerformed

