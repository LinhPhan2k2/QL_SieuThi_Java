    private void updateRowHeights()
    {
        for (int row = 0; row < tbl_danhsachHanghoa.getRowCount(); row++)
        {
            int rowHeight = tbl_danhsachHanghoa.getRowHeight();

            for (int column = 0; column < tbl_danhsachHanghoa.getColumnCount(); column++)
            {
                Component comp = tbl_danhsachHanghoa.prepareRenderer(tbl_danhsachHanghoa.getCellRenderer(row, column), row, column);
                rowHeight = Math.max(rowHeight, comp.getPreferredSize().height);
            }

            tbl_danhsachHanghoa.setRowHeight(row, rowHeight);
        }
              
    }
