    private void HienThi_DSKH() {
        ArrayList<KhachHang> dsKH = KhachHangDAO.layDSKH();
        DefaultTableModel dtb = (DefaultTableModel)tbl_dktheTv.getModel();
        dtb.setRowCount(0);
        for(KhachHang kh : dsKH)
        {
            Vector<Object> vec = new Vector<Object>();
            vec.add(kh.getMaKh());
            vec.add(kh.getTenKh());
            vec.add(kh.getDiaChi());
            vec.add(kh.getSoDT());
            vec.add(kh.getDiem());                                 
            dtb.addRow(vec);
        }
        tbl_dktheTv.setModel(dtb);
        
    }

