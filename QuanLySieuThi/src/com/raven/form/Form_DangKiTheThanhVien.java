        private void updateRowHeights()
    {
        for (int row = 0; row < tbl_dktheTv.getRowCount(); row++)
        {
            int rowHeight = tbl_dktheTv.getRowHeight();

            for (int column = 0; column < tbl_dktheTv.getColumnCount(); column++)
            {
                Component comp = tbl_dktheTv.prepareRenderer(tbl_dktheTv.getCellRenderer(row, column), row, column);
                rowHeight = Math.max(rowHeight, comp.getPreferredSize().height);
            }

            tbl_dktheTv.setRowHeight(row, rowHeight);
        }
              
    }
