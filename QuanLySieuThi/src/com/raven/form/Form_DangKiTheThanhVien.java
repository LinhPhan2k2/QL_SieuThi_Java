    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        if(txt_diaChi.getText().isEmpty() ||txt_sdt.getText().isEmpty()||txt_tenKH.getText().isEmpty())
    {
      JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
    }  
    else
    {                                  
                KhachHang kh = new KhachHang();
                int mathe= Integer.parseInt(txt_maThe.getText());
                kh.setMaKh(mathe);
                kh.setTenKh(txt_tenKH.getText());
                kh.setDiaChi(txt_diaChi.getText());                       
                kh.setSoDT(txt_sdt.getText());                
                boolean kq= KhachHangDAO.suaKH(kh);
                if(kq)
                {
                  JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                }
                else
                {
                  JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }                                                                                                 
      }
        HienThi_DSKH();  
         updateRowHeights();
    }//GEN-LAST:event_btn_SuaActionPerformed

