package pojo;

/**
 *
 * @author LinhPTA-PC
 */
public class Apriori {
    String mahh, tenhh;

    public Apriori(String mahh, String tenhh) {
        this.mahh = mahh;
        this.tenhh = tenhh;
    }

    public Apriori() {
    }

    public String getMahh() {
        return mahh;
    }

    public void setMahh(String mahh) {
        this.mahh = mahh;
    }

    public String getTenhh() {
        return tenhh;
    }

    public void setTenhh(String tenhh) {
        this.tenhh = tenhh;
    }
    
    
    
}
